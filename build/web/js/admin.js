window.onload = (event) => {
  document.getElementById("cntopic").style.visibility="hidden";
  document.getElementById("ostopic").style.visibility="hidden";
  document.getElementById("pytopic").style.visibility="hidden";
  document.getElementById("aitopic").style.visibility="hidden";
  document.getElementById("toctopic").style.visibility="hidden";
  document.getElementById("networkstopic").style.visibility="hidden";
};
function cn()
{
        if(document.getElementById("cntopic").style.visibility==="hidden")
        {
            document.getElementById("cntopic").style.visibility="visible";
        }
        else if(document.getElementById("cntopic").style.visibility==="visible")
        {
            document.getElementById("cntopic").style.visibility="hidden";
        }
}
